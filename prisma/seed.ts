async function main() {
  console.debug('Iniciando seed.');
  await database.$transaction(async (tx) => {
    for (const ambiente of ambientes) {
      await tx.ambiente.upsert({
        where: {
          codigo_localizacao: {
            codigo: ambiente.codigo,
            localizacao: ambiente.localizacao,
          },
        },
        update: {},
        create: {
          id: uuidv7(),
          ...ambiente,
          Chave: {
            create: {
              id: uuidv7(),
              tipo: 'AMBIENTE',
            },
          },
        },
      });
    }
  });
  console.log('Ambientes Criados.');
  await database.$transaction(async (tx) => {
    for (const armario of armarios) {
      await tx.armario.upsert({
        where: {
          codigo: armario.codigo,
        },
        update: {},
        create: {
          id: uuidv7(),
          codigo: armario.codigo,
          andar: `${armario.codigo.charAt(1)}° Andar`,
          localizacao: 'SNO',
          bloco: `Bloco ${armario.codigo.charAt(0)}`,
          Chave: {
            create: {
              id: uuidv7(),
              tipo: 'ARMARIO',
            },
          },
        },
      });
    }
  });
  console.log('Armarios Criados');
  for (const usuario of usuarios) {
    await database.usuarios.upsert({
      where: {
        matricula: usuario.matricula,
      },
      update: {},
      create: {
        id: usuario.id ?? uuidv7(),
        matricula: usuario.matricula,
        nome: usuario.nome,
      },
    });
  }
  console.log('Usuários Criados.');

  const hashedPassword = await bcrypt.hash(process.env.ADMIN_PASSWORD!, 10);
  await database.operadores.upsert({
    where: {
      email: process.env.ADMIN_EMAIL!,
    },
    update: {},
    create: {
      id: uuidv7(),
      name: process.env.ADMIN_NAME!,
      email: process.env.ADMIN_EMAIL!,
      emailVerified: true,
      role: 'ADMIN',
      accounts: {
        create: {
          id: uuidv7(),
          providerId: 'credential',
          password: hashedPassword,
          accountId: 'credential',
        },
      },
    },
  });
  console.log('ADMIN criado.');
}

await main()
  .then(() => {
    console.warn('Seed finalizado.');
    process.exit(0);
  })
  .catch((err) => {
    console.warn('Erro!');
    console.error(err);
    process.exit(1);
  });
